/*******************************************************************************
 * Includes
 ******************************************************************************/
#include <stdbool.h>

/*******************************************************************************
 * Global variables
 ******************************************************************************/
extern int erm_test_start(void);

/*******************************************************************************
 * Main
 ******************************************************************************/
int main(void)
{
    /* Call function */
    bool exit_code = erm_test_start() ? 0U : 1U; /* exit_code = 0U (FAIL): exit_code = 1U (PASS) */

    /* Infinite loop */
    for (;;)
    {
        if (exit_code != 0)
        {
            break;
        }
    }

    return exit_code;
}

